import math 

print(math.pi) # 3.14159265358979

print(math.ceil(1.9)) # 2, ceiling 올림

print(math.floor(1.9)) # 1, floor 내림

print(math.trunc(1.9)) # 1, 절사

print(math.floor(-1.9)) # -2, floor 내림
print(math.trunc(-1.9)) # -1, 잘사

print(math.sqrt(4)) # 2.0, 제곱근 square root
print(math.pow(2, 3)) # 8.0, 제곱 
